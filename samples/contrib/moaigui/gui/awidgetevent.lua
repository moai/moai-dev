local _M = {}

require "gui\\support\\class"

aevent = require "gui\\aevent"

_M.AWidgetEvent = class(aevent.AEvent)

function _M.AWidgetEvent:init(eventType, widget)
	aevent.AEvent.init(self, eventType)

	self.widget = widget
end

return _M
